# For Loop
planet = "Earth"

for i in planet:
    print("Value of i is now:", i)

print("Rest of the code.")

vaccines = ("Moderna", "Pfizer", "Sputnik v", "Covaxin", "AstraZeneca")

for v in vaccines:
    print(f"{v} vaccine provides immunization against covid19.")

# While Loop
x = 0
while x <= 10:
    print("Value of X is:", x)
    print("Looping...")
    x += 1

print("Rest of the code.")


